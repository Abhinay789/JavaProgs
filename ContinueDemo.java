public class ContinueDemo {
    public static void main(String[] args) {
        int i,j;
        for(i=1;i<=7;i++)//rows
        {
            for(j=1;j<=i;j++)//cols
            {
                  System.out.print(" "+(j));
            }
            System.out.println();
           
        }
    }
    
}
